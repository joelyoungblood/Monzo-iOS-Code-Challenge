import UIKit

extension UIColor {
    
    static var headlinesLightGrey: UIColor {
        return UIColor(hex: "ECECEC")
    }
    
    static var headlinesDarkGrey: UIColor {
        return UIColor(hex: "333333")
    }
    
    static var headlinesGold: UIColor {
        return UIColor(hex: "FFCD00")
    }
}
